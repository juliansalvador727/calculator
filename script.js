const add = (a, b) => {
  return a + b;
};

const subtract = (a, b) => {
  return a - b;
};

const multiply = (a, b) => {
  return a * b;
};

const divide = (a, b) => {
  return a / b;
};

const pi = 3.14;
const e = 2.71;

console.log(add(e, pi));
console.log(subtract(pi, e));
console.log(multiply(pi, e));
console.log(divide(pi, e));
